public class Nilai {
    private int nim;
    private String kd_mk;
    private String nilai;

    public int getNim() {
        return nim;
    }

    public void setNim(int nim) {
        this.nim = nim;
    }

    public String getMk() {
        return kd_mk;
    }

    public void setMk(String kd_mk) {
        this.kd_mk = kd_mk;
    }

    public String getNilai() {
        return nilai;
    }

    public void setNilai(String nilai) {
        this.nilai = nilai;
    }
}
